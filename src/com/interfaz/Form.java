    public void agregarVendedor(){
        String calif="";
        if(radio1.isSelected()==true){
            calif="Malo";
        }
        else if(radio2.isSelected()==true){
            calif="Regular";
        }
        else if(radio3.isSelected()==true){
            calif="Bueno";
        }
        else if(radio4.isSelected()==true){
            calif="Excelente";
        }
        
        vendedor.ingresarVendedor(codVendTxt.getText(),cedTxt.getText()
                ,nomTxt.getText(), apelTxt.getText(), telTxt.getText(),
                tipoVend.getSelectedItem().toString(),calif);
    }
    
    public void agregarPedido() {

        Producto articulo = new Producto(this.codArtTxt.getText(),
                this.artTxt.getText(), Double.valueOf(this.precioTxt.getText()));
        Detalle detalle = new Detalle(articulo, Integer.valueOf(unidadTxt.getText()));
        detalles.add(detalle);
        
        Object[] filas = {detalle.getProducto().getCodArticulo(),
            detalle.getProducto().getNomArticulo(),
            detalle.getProducto().getPrecio(),
            detalle.getUnidad(),
            detalle.getTotalUnidad()};

        modelo.addRow(filas);
        tabla.setModel(modelo);
        tabla.updateUI();
      
    }
    
    public void limpiarTabla(){
       
        int filas=tabla.getRowCount();
            for (int i = 0;filas>i; i++) {
                modelo.removeRow(0);
            }       
        tabla.setModel(modelo);
        tabla.updateUI();
         codArtTxt.requestFocus();
    }
    
    public void limpiarArticulo(){
        codArtTxt.setText("");
        artTxt.setText("");
        precioTxt.setText("");
        unidadTxt.setText("");
        codArtTxt.requestFocus();
    }

    
